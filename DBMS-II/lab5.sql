--1.Create a cursor Course_Cursor to fetch all rows from COURSE table and display them.
	
	DECLARE @SID INT,@SNAME VARCHAR(50),@SEMAIL VARCHAR(50),@SPHONE VARCHAR(50),@SDEPT VARCHAR(50),@SBDATE DATE,@SENROLL INT
	
	DECLARE COURSE_CURSOR CURSOR
	FOR
	SELECT StudentID,StuName,StuEmail,StuPhone,StuDepartment,StuDateOfBirth,StuEnrollmentYear FROM STUDENT
	OPEN COURSE_CURSOR
	FETCH NEXT FROM COURSE_CURSOR INTO @SID,@SNAME,@SEMAIL,@SPHONE,@SDEPT,@SBDATE,@SENROLL
	WHILE @@FETCH_STATUS=0
	BEGIN
		SELECT @SID,@SNAME,@SEMAIL,@SPHONE,@SDEPT,@SBDATE,@SENROLL
		FETCH NEXT FROM COURSE_CURSOR INTO @SID,@SNAME,@SEMAIL,@SPHONE,@SDEPT,@SBDATE,@SENROLL
	END
	CLOSE COURSE_CURSOR
	DEALLOCATE COURSE_CURSOR

