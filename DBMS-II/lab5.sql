--8. Create a cursor to update all NULL grades to 'F' for enrollments with Status 'Completed'

	DECLARE 
		@ENROLLMENTID INT,
		@GRADE VARCHAR(2)

	DECLARE ENROLLMENT_UPDATE_CURSOR CURSOR
	FOR
	SELECT ENROLLMENTID,ENROLLMENTSTATUS
		   GRADE
	FROM ENROLLMENT
	WHERE ENROLLMENTSTATUS = 'COMPLETED'

	OPEN ENROLLMENT_UPDATE_CURSOR

	FETCH NEXT FROM ENROLLMENT_UPDATE_CURSOR
	INTO @ENROLLMENTID,
		 @GRADE

	WHILE @@FETCH_STATUS = 0
	BEGIN
		IF @GRADE IS NULL
		BEGIN
			UPDATE ENROLLMENT
			SET GRADE = 'F'
			WHERE ENROLLMENTID = @ENROLLMENTID
		END

		FETCH NEXT FROM ENROLLMENT_UPDATE_CURSOR
		INTO @ENROLLMENTID,
			 @GRADE
	END

	CLOSE ENROLLMENT_UPDATE_CURSOR
	DEALLOCATE ENROLLMENT_UPDATE_CURSOR

--9. Cursor to show Faculty with Course they teach (EX: Dr. Sheth teaches Data structure)
	DECLARE 
		@FACULTYNAME VARCHAR(100),
		@COURSENAME VARCHAR(100)

	DECLARE FACULTY_COURSE_CURSOR CURSOR
	FOR
	SELECT F.FACULTYNAME, C.COURSENAME
	FROM FACULTY F
	JOIN COURSE FC ON F.FACULTYID = F.FACULTYID
	JOIN COURSE C ON FC.COURSEID = C.COURSEID

	OPEN FACULTY_COURSE_CURSOR

	FETCH NEXT FROM FACULTY_COURSE_CURSOR
	INTO @FACULTYNAME, @COURSENAME

	WHILE @@FETCH_STATUS = 0
	BEGIN
		PRINT @FACULTYNAME + ' TEACHES ' + @COURSENAME

		FETCH NEXT FROM FACULTY_COURSE_CURSOR
		INTO @FACULTYNAME, @COURSENAME
	END

	CLOSE FACULTY_COURSE_CURSOR
	DEALLOCATE FACULTY_COURSE_CURSOR

