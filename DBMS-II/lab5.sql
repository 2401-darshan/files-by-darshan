--5. Create a cursor Course_CursorUpdate that retrieves all courses and increases Credits by 1 for courses
--with Credits less than 4.

	DECLARE @CID VARCHAR(50),@CCREDITS INT
	
	DECLARE COURSE_CURSORUPDATE_CURSOR CURSOR
	FOR
	SELECT CourseID,CourseCredits FROM COURSE 
	OPEN COURSE_CURSORUPDATE_CURSOR
	FETCH NEXT FROM COURSE_CURSORUPDATE_CURSOR INTO @CID,@CCREDITS
	WHILE @@FETCH_STATUS=0
	BEGIN
		UPDATE COURSE SET CourseCredits=CourseCredits + 1 WHERE CourseID=@CID
		FETCH NEXT FROM COURSE_CURSORUPDATE_CURSOR INTO @CID,@CCREDITS
	END
	CLOSE COURSE_CURSORUPDATE_CURSOR
	DEALLOCATE COURSE_CURSORUPDATE_CURSOR

--6. Create a Cursor to fetch Student Name with Course Name (Example: Raj Patel is enrolled in Database
--Management System)

	DECLARE @SNAME VARCHAR(50),@CNAME VARCHAR(50)
	
	DECLARE STU_FETCH_CURSOR CURSOR
	FOR
	SELECT StuName,CourseName 
	FROM ENROLLMENT JOIN STUDENT
	ON ENROLLMENT.StudentID=STUDENT.StudentID
	JOIN COURSE
	ON ENROLLMENT.CourseID=COURSE.CourseID
	OPEN STU_FETCH_CURSOR
	FETCH NEXT FROM STU_FETCH_CURSOR INTO @SNAME,@CNAME
	WHILE @@FETCH_STATUS=0
	BEGIN
		PRINT @SNAME + ' is enrolled in ' + @CNAME
		FETCH NEXT FROM STU_FETCH_CURSOR INTO @SNAME,@CNAME
	END
	CLOSE STU_FETCH_CURSOR
	DEALLOCATE STU_FETCH_CURSOR

