--10. Cursor to calculate total credits per student (Example: Raj Patel has total credits = 15)
	DECLARE 
		@STUDENTID INT,
		@STUNAME VARCHAR(100),
		@TOTALCREDITS INT

	DECLARE STUDENT_CREDIT_CURSOR CURSOR
	FOR
	SELECT S.STUDENTID,
		   S.STUNAME,
		   SUM(C.COURSECREDITS)
	FROM STUDENT S
	JOIN ENROLLMENT E ON S.STUDENTID = E.STUDENTID
	JOIN COURSE C ON E.COURSEID = C.COURSEID
	GROUP BY S.STUDENTID,S.STUNAME

	OPEN STUDENT_CREDIT_CURSOR

	FETCH NEXT FROM STUDENT_CREDIT_CURSOR
	INTO @STUDENTID,
		 @STUNAME,
		 @TOTALCREDITS

	WHILE @@FETCH_STATUS = 0
	BEGIN
		PRINT @STUNAME + ' HAS TOTAL CREDITS = ' + CAST(@TOTALCREDITS AS VARCHAR)

		FETCH NEXT FROM STUDENT_CREDIT_CURSOR
		INTO @STUDENTID,
			 @STUNAME,
			 @TOTALCREDITS
	END

	CLOSE STUDENT_CREDIT_CURSOR
	DEALLOCATE STUDENT_CREDIT_CURSOR	
